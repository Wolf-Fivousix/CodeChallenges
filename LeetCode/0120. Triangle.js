// Given a triangle array, return the minimum path sum from top to bottom.
// For each step, you may move to an adjacent number of the row below. More formally, if you are on index i on the current row, you may move to either index i or index i + 1 on the next row.

// Example 1:
// Input: triangle = [[2],[3,4],[6,5,7],[4,1,8,3]]
// Output: 11
// Explanation: The triangle looks like:
//    2
//   3 4
//  6 5 7
// 4 1 8 3
// The minimum path sum from top to bottom is 2 + 3 + 5 + 1 = 11 (underlined above).

// Example 2:
// Input: triangle = [[-10]]
// Output: -10
 

// Constraints:

// 1 <= triangle.length <= 200
// triangle[0].length == 1
// triangle[i].length == triangle[i - 1].length + 1
// -104 <= triangle[i][j] <= 104
 

// Follow up: Could you do this using only O(n) extra space, where n is the total number of rows in the triangle?

/*
BRUTE FORCE
We want to calculate every single possible path
And return the minimum one

Linear Time Complexity O(N) - because we will visit every single element.
Greater than Linear Space Complexit O(N) - because we'll have as the input grows, the number of paths will increase as well
Nononoo! For every element we're creating TWO branching logics! This is not linear, this is polynomial!

*/

/**
 * @param {number[][]} triangle
 * @return {number}
 */
function minimumTotalBruteForce(triangle) {
    const lastRow = triangle.length - 1
    if (lastRow === 0) return triangle[0][0]

    let minimumPathSum = Number.POSITIVE_INFINITY
    const pathStack = [{
        index: 0,
        row: 0,
        sum: triangle[0][0],
    }]
    while (pathStack.length) {
        const currentPath = pathStack.pop()
        if (currentPath.row === lastRow) {
            minimumPathSum = Math.min(minimumPathSum, currentPath.sum)
            continue
        }

        // Same index
        pathStack.push({
            index: currentPath.index,
            row: currentPath.row + 1,
            sum: currentPath.sum + triangle[currentPath.row + 1][currentPath.index]
        })
        // Next index
        pathStack.push({
            index: currentPath.index + 1,
            row: currentPath.row + 1,
            sum: currentPath.sum + triangle[currentPath.row + 1][currentPath.index + 1]
        })
    }

    return minimumPathSum
};

