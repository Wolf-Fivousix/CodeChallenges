// So let's refactor the solution to not rely on a gigantic stack of possibilities.
// How come? Very simple: When we are calculating one element of the array, we have 2 possible "origins".
// Although we don't know what numbers are yet to come, we DO KNOW what numbers have been IN THE PAST.
// So instead of branching the logic to multiple paths that we already know WILL NOT be smaller, we can simply cut them off
//by taking the min value between the 2 parents! This way we exclude the whole branch from the computation!

/**
 * @param {number[][]} triangle
 * @return {number}
 */
function minimumTotal(triangle) {
    const lastRow = triangle.length - 1
    if (lastRow === 0) return triangle[0][0]

    const sumMatrix = [triangle[0]]

    for (let i = 1; i <= lastRow; ++i) {
        const newSumRow = []
        for (let j = 0; j < triangle[i].length; ++j) {
            const leftParent = j - 1 >= 0 ? triangle[i][j] + sumMatrix[i - 1][j - 1] : Number.POSITIVE_INFINITY
            const rightParent = j < sumMatrix[i - 1].length ? triangle[i][j] + sumMatrix[i - 1][j] : Number.POSITIVE_INFINITY
            const currentMinSum = Math.min(leftParent, rightParent)
            newSumRow.push(currentMinSum)
        }
        sumMatrix.push(newSumRow)
    }

    // console.log(sumMatrix[sumMatrix.length - 1])
    return sumMatrix[sumMatrix.length - 1].reduce((minSum, element) => Math.min(minSum, element))
};

